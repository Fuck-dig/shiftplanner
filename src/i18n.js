// makeT(lang) → t(key, vars) with {token} interpolation.
// t.n(baseKey, count, vars) picks `${baseKey}.one` / `${baseKey}.other`.
export function makeT(lang){
  const dict = STRINGS[lang] || STRINGS.en;
  const fb   = STRINGS.en;
  const t = (key, vars) => {
    let s = (dict[key] != null) ? dict[key] : (fb[key] != null ? fb[key] : key);
    if(vars) for(const k in vars) s = s.split('{'+k+'}').join(String(vars[k]));
    return s;
  };
  t.n = (baseKey, count, vars) => t(`${baseKey}.${count===1?'one':'other'}`, { ...(vars||{}), n: count });
  return t;
}