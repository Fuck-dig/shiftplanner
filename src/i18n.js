export const LOCALES = { da: 'da-DK', en: 'en-GB', es: 'es-ES' };

export function detectLang(){
  try{
    const l=(navigator.language||'en').slice(0,2).toLowerCase();
    if(l==='da') return 'da';
    if(l==='es') return 'es';
  }catch{}
  return 'en';
}

